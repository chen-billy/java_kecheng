package cz0227;

/*
 * 定义数组以及访问数组中的元素
 */
public class Ex24 {
	public static void main(String[] args) {
		int[] arr;
		arr = new int[3];
		System.out.println("arr[0]=" + arr[0]);
		System.out.println("arr[1]=" + arr[1]);
		System.out.println("arr[2]=" + arr[2]);
		System.out.println("数组的长度是：" + arr.length);
	}
}
